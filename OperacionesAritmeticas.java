
package operacionesaritmeticas;


public class OperacionesAritmeticas {

   
    public static void main(String[] args) {
       System.out.println("Siendo mis dos números enteros 12 y 10");
       System.out.println("Se imprime en consola las operaciones aritméticas" +"\n");
       
       
        int n1=12;
        int n2=10;
                
        int suma,resta,multi,div,mod;
        
        suma= n1+n2;
        resta=n1-n2;
        multi=n1*n2;
        div=n1/n2;
        mod=n1%n2;
        
        
        System.out.println("***Operaciones aritméticas***" + "\n");
         System.out.println("La suma de los enteros es:" + suma);
         System.out.println("La resta de los enteros es:" + resta);
         System.out.println("La multiplicación de los enteros es:" + multi);
         System.out.println("La división de los enteros es:" + div);
         System.out.println("El mod de los enteros es:" + mod);
         System.out.println("\n");
         
         
         
        
        
    }
    
}
